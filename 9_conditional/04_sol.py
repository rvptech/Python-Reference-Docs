# Fruit Ripeness Checker

# Determine if a fruit is ripe, overripe, or unripe based on its color. (e.g., Banana: Green - Unripe, Yellow - Ripe, Brown - Overripe)

fdata = input("Enter the color of the banana : ")

if (fdata == "Green"):
    print("Your Banana is Unripe")
elif (fdata == "Yellow"):
    print("Your Banana is Ripe")
elif (fdata == "Brown"):
    print("Your Banana is Overripe")
